async def _auth(client) -> dict:
    resp = await client.post("/api/auth/login", json=ADMIN)
    return {"Authorization": f"Bearer {resp.json()['access_token']}"}


async def _ingest(client, headers, title="Norme entretien DGR") -> dict:
    pdf = _make_pdf([PAGE_COLMATAGE, PAGE_RESURFACAGE])
    resp = await client.post(
        "/api/knowledge/documents",
        data={"title": title, "doc_type": "NORME_DGR", "source": "DGR", "version": "2024"},
        files={"file": ("norme.pdf", pdf, "application/pdf")},
        headers=headers,
    )
    assert resp.status_code == 201, resp.text
    return resp.json()


# ---------------- Chunking unit ----------------


def test_chunker_windows_and_pages() -> None:
    """600 + 100 words, 512/50: chunk 0 = 512 words page 1; chunk 1 starts at
    word 462 (overlap 50) -> 238 words spanning pages 1-2."""
    chunks = chunk_pages(["mot " * 600, "fin " * 100], chunk_size=512, overlap=50)
    assert [c.index for c in chunks] == [0, 1]
    assert (chunks[0].page_start, chunks[0].page_end) == (1, 1)
    assert (chunks[1].page_start, chunks[1].page_end) == (1, 2)
    assert len(chunks[1].text.split()) == 238
    with pytest.raises(ValueError):
        chunk_pages(["x"], chunk_size=50, overlap=50)


