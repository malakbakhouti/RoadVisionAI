async def test_ingest_pdf_extracts_chunks_and_indexes(client, chroma) -> None:
    headers = await _auth(client)
    doc = await _ingest(client, headers)
    assert doc["status"] == "INDEXED"
    assert doc["page_count"] == 2
    assert doc["embedding_count"] >= 1  # short doc -> 1 chunk
    # vectors really are in Chroma
    collection = chroma.get_or_create_collection("normative_corpus")
    stored = collection.get(where={"document_id": doc["id"]})
    assert len(stored["ids"]) == doc["embedding_count"]

    listing = await client.get("/api/knowledge/documents", headers=headers)
    assert any(d["id"] == doc["id"] for d in listing.json())


async def test_ingest_rejects_invalid_inputs(client) -> None:
    headers = await _auth(client)
    bad_type = await client.post(
        "/api/knowledge/documents",
        data={"title": "Doc", "doc_type": "INVALIDE"},
        files={"file": ("x.pdf", b"%PDF-fake", "application/pdf")},
        headers=headers,
    )
    assert bad_type.status_code == 422
    not_pdf = await client.post(
        "/api/knowledge/documents",
        data={"title": "Doc", "doc_type": "ASTM"},
        files={"file": ("x.pdf", b"ceci n'est pas un pdf", "application/pdf")},
        headers=headers,
    )
    assert not_pdf.status_code == 400


# ---------------- Retrieval: ranking + threshold, hand-computed ----------------


async def test_search_ranks_relevant_chunk_first(client) -> None:
    """Query 'colmatage fissure' -> the colmatage page must hit with high
    similarity and carry its citation metadata (title + pages)."""
    headers = await _auth(client)
    doc = await _ingest(client, headers, title="Norme colmatage")

    resp = await client.post(
        "/api/knowledge/search",
        json={"query": "colmatage fissure", "threshold": 0.5},
        headers=headers,
    )
    assert resp.status_code == 200
    hits = resp.json()["hits"]
    assert len(hits) >= 1
    top = hits[0]
    assert "colmatage" in top["text"].lower()
    assert top["title"] == "Norme colmatage"
    assert top["document_id"] == doc["id"]
    assert top["page_start"] >= 1
    assert top["similarity"] > 0.5


async def test_threshold_filters_unrelated_queries(client) -> None:
    """Query 'béton' shares no axis with the corpus: similarity stays low and
    the CDC threshold 0.75 must filter everything out."""
    headers = await _auth(client)
    await _ingest(client, headers)

    resp = await client.post(
        "/api/knowledge/search",
        json={"query": "béton", "threshold": 0.75},
        headers=headers,
    )
    assert resp.status_code == 200
    assert resp.json()["hits"] == []  # nothing citable below threshold


async def test_get_document_and_404(client) -> None:
    headers = await _auth(client)
    doc = await _ingest(client, headers)
    detail = await client.get(f"/api/knowledge/documents/{doc['id']}", headers=headers)
    assert detail.status_code == 200
    missing = await client.get(f"/api/knowledge/documents/{uuid.uuid4()}", headers=headers)
    assert missing.status_code == 404
